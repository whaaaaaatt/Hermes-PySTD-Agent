"""Context compression — keep long conversations from blowing the
context window.

Two strategies, both stdlib-only:

1. **Count-based compression** (default, no API call):
   Estimate the number of "tokens" in the message history using a
   rough heuristic (4 chars per token). When the total exceeds
   ``threshold``, summarise the oldest block of messages into a single
   user-role message and keep the most recent ``target`` messages
   verbatim.

2. **Model-summarised compression** (optional, makes one cheap call):
   Same as above, but the summary is generated by calling the active
   provider with a "summarise this conversation" prompt. This produces
   better summaries at the cost of an extra round trip. The summary
   model defaults to the main model but can be overridden via config.

We deliberately do NOT use tiktoken (a third-party tokenizer). The
character-based estimate is good enough to drive compression decisions
and stays at zero dependencies.
"""
from __future__ import annotations

import hashlib
import json
import logging
import re
import time
from dataclasses import dataclass
from typing import Any, Dict, List, Optional, Set, Tuple

from ..providers import ChatMessage, OpenAICompatProvider, ProviderProfile
from ..state import Message, StateStore

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Token estimation
# ---------------------------------------------------------------------------

# 4 characters per token is a well-known rough estimate. The exact
# ratio depends on the model, but for triggering compression it's
# good enough. We're not trying to be precise — we want a stable
# signal that doesn't depend on a tokenizer.

CHARS_PER_TOKEN = 4


def estimate_tokens(text: str) -> int:
    """Estimate the token count for a piece of text. Pure-Python, no deps."""
    if not text:
        return 0
    return max(1, (len(text) + CHARS_PER_TOKEN - 1) // CHARS_PER_TOKEN)


def estimate_messages_tokens(messages: List[Message]) -> int:
    """Sum the estimated token count across messages.

    Adds 4 tokens per message for role / metadata overhead — same
    shape OpenAI charges.
    """
    total = 0
    for m in messages:
        total += 4
        if m.content:
            total += estimate_tokens(m.content)
        if m.tool_calls:
            try:
                total += estimate_tokens(json.dumps(m.tool_calls, ensure_ascii=False))
            except (TypeError, ValueError):
                pass
    return total


# ---------------------------------------------------------------------------
# Compression
# ---------------------------------------------------------------------------

@dataclass
class CompressionResult:
    """What happened during a compress() call."""
    triggered: bool
    tokens_before: int
    tokens_after: int
    messages_before: int
    messages_after: int
    method: str  # "count" | "summary" | "skipped"
    summary: str = ""


def _format_messages_for_summary(messages: List[Message]) -> str:
    """Render the messages as plain text suitable for the summary prompt."""
    lines: List[str] = []
    for m in messages:
        role = m.role.upper()
        body = (m.content or "").strip()
        if not body and not m.tool_calls:
            continue
        if m.tool_calls:
            tc_text = "; ".join(
                f"{((tc.get('function') or {}).get('name') or '?')}(...)" for tc in m.tool_calls
            )
            body = (body + "\n  [tool calls: " + tc_text + "]").strip()
        lines.append(f"[{role}] {body}")
    return "\n\n".join(lines)


_SUMMARY_PROMPT = (
    "You are a context compressor. Summarise the following conversation "
    "in 4-8 bullet points. Preserve: user goals, decisions made, file paths "
    "and tool outputs that matter, and any open questions. Drop pleasantries. "
    "Do NOT add new information. Use third-person present tense."
)


def _summarise_via_model(
    messages_text: str,
    profile: ProviderProfile,
    model: str,
) -> str:
    """Call the model to produce a summary. Returns "" on failure."""
    try:
        client = OpenAICompatProvider(profile)
        result = client.chat(
            [
                ChatMessage(role="system", content=_SUMMARY_PROMPT),
                ChatMessage(role="user", content=f"Conversation so far:\n\n{messages_text}"),
            ],
            model=model,
            stream=False,
            max_tokens=600,
        )
        return result.content.strip()
    except Exception as exc:  # noqa: BLE001
        logger.warning("compress: model summary failed: %s", exc)
        return ""


def _count_based_summary(messages: List[Message]) -> str:
    """Build a cheap, deterministic summary from message metadata.

    We don't have the model's words; we just describe *what* the
    conversation touched on. This is intentionally rough — the
    detailed content is still in the recent messages.
    """
    user_msgs = [m for m in messages if m.role == "user"]
    asst_msgs = [m for m in messages if m.role == "assistant"]
    tool_msgs = [m for m in messages if m.role == "tool"]
    tool_calls_total = sum(len(m.tool_calls or []) for m in asst_msgs)

    # Extract topics from user messages.
    topics: List[str] = []
    for m in user_msgs[:5]:
        line = (m.content or "").splitlines()[0][:100].strip()
        if line and line not in topics:
            topics.append(line)

    # List tool names used.
    tool_names: List[str] = []
    for m in asst_msgs:
        for tc in (m.tool_calls or []):
            name = (tc.get("function") or {}).get("name", "")
            if name and name not in tool_names:
                tool_names.append(name)

    lines = [
        "## Conversation Summary",
        "",
        f"- {len(user_msgs)} user messages, {len(asst_msgs)} assistant messages, {len(tool_msgs)} tool results",
        f"- {tool_calls_total} tool call(s) executed",
    ]
    if tool_names:
        lines.append(f"- Tools used: {', '.join(tool_names)}")
    if topics:
        lines.append("- Topics discussed:")
        for t in topics:
            lines.append(f"  - {t!r}")
    first_user = (user_msgs[0].content or "").splitlines()[0][:120] if user_msgs else ""
    last_user = (user_msgs[-1].content or "").splitlines()[0][:120] if user_msgs else ""
    if first_user:
        lines.append(f"- First user message: {first_user!r}")
    if last_user and last_user != first_user:
        lines.append(f"- Last user message: {last_user!r}")
    return "\n".join(lines)


