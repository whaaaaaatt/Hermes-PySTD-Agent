def _summarize_tool_result(tool_name: str, tool_args: dict, content: str) -> str:
    """Generate a one-line summary for a tool result, preserving key info."""
    if not content:
        return f"[{tool_name}] (empty result)"
    content_len = len(content)
    lines_count = content.count("\n") + 1

    if tool_name == "terminal":
        # Extract exit code and line count.
        exit_match = content[:50]
        return f"[terminal] {tool_args.get('command', '?')[:60]} -> {lines_count} lines, {content_len} chars"
    if tool_name == "read_file":
        path = tool_args.get("path", "?")
        return f"[read_file] {path} ({content_len} chars, {lines_count} lines)"
    if tool_name == "write_file":
        path = tool_args.get("path", "?")
        return f"[write_file] {path} ({content_len} chars)"
    if tool_name == "edit_file":
        path = tool_args.get("path", "?")
        return f"[edit_file] {path} ({content_len} chars)"
    if tool_name == "list_dir":
        return f"[list_dir] {tool_args.get('path', '?')} ({lines_count} entries)"
    if tool_name in ("http_get", "web_extract"):
        url = tool_args.get("url", "?")[:80]
        return f"[{tool_name}] {url} ({content_len} chars)"
    if tool_name == "web_search":
        query = tool_args.get("query", "?")[:60]
        return f"[web_search] {query} ({content_len} chars)"
    if tool_name == "python_exec":
        code_preview = (tool_args.get("code") or "")[:60]
        return f"[python_exec] {code_preview}... -> {lines_count} lines"
    if tool_name == "memory":
        action = tool_args.get("action", "?")
        key = tool_args.get("key", "")
        return f"[memory] {action} {key} ({content_len} chars)"
    if tool_name == "git_status":
        return f"[git_status] ({lines_count} lines)"
    if tool_name == "git_diff":
        return f"[git_diff] ({content_len} chars)"
    if tool_name == "git_log":
        return f"[git_log] ({lines_count} entries)"
    # Generic fallback.
    args_preview = json.dumps(tool_args, ensure_ascii=False)[:80]
    return f"[{tool_name}] {args_preview} -> {content_len} chars"


def _truncate_tool_call_args(tool_calls: Optional[List[Dict[str, Any]]]) -> Optional[List[Dict[str, Any]]]:
    """Truncate large string values inside tool call arguments.

    Preserves valid JSON by recursively shrinking strings >200 chars.
    """
    if not tool_calls:
        return tool_calls
    _MAX_ARG_STR = 200
    def _shrink(obj):
        if isinstance(obj, str):
            if len(obj) > _MAX_ARG_STR:
                return obj[:_MAX_ARG_STR] + "...[truncated]"
            return obj
        if isinstance(obj, dict):
            return {k: _shrink(v) for k, v in obj.items()}
        if isinstance(obj, list):
            return [_shrink(v) for v in obj]
        return obj
    result = []
    for tc in tool_calls:
        tc = dict(tc)
        fn = tc.get("function") or {}
        args_raw = fn.get("arguments", "")
        if args_raw and isinstance(args_raw, str):
            try:
                args_obj = json.loads(args_raw)
                args_obj = _shrink(args_obj)
                tc = {**tc, "function": {**fn, "arguments": json.dumps(args_obj, ensure_ascii=False)}}
            except (json.JSONDecodeError, ValueError):
                pass
        result.append(tc)
    return result


def _deduplicate_tool_results(messages: List[Message]) -> int:
    """Replace duplicate tool results with a placeholder. Returns count removed."""
    seen: Dict[str, int] = {}  # md5 -> index in messages
    removed = 0
    for i, m in enumerate(messages):
        if m.role != "tool" or not m.content:
            continue
        import hashlib
        h = hashlib.md5(m.content.encode("utf-8", errors="replace")).hexdigest()
        if h in seen:
            m.content = "[Duplicate tool output — same content as a more recent call]"
            removed += 1
        else:
            seen[h] = i
    return removed


def _cleanup_orphaned_pairs(messages: List[Message]) -> int:
    """After compression, remove orphaned tool_call / tool_result pairs.

    If a tool_call has no matching tool_result (or vice versa), add a stub.
    Returns number of stubs added.
    """
    # Collect all tool_call_ids that have results.
    result_ids: Set[str] = set()
    for m in messages:
        if m.role == "tool" and m.tool_call_id:
            result_ids.add(m.tool_call_id)
    # Collect all tool_call_ids that were emitted.
    call_ids: Set[str] = set()
    for m in messages:
        if m.role == "assistant" and m.tool_calls:
            for tc in m.tool_calls:
                tc_id = tc.get("id", "")
                if tc_id:
                    call_ids.add(tc_id)
    stubs = 0
    # orphan results: tool results with no matching tool_call
    # (happens when assistant messages were compressed away)
    orphan_result_ids = result_ids - call_ids
    # orphan calls: tool calls with no matching result
    orphan_call_ids = call_ids - result_ids
    return stubs


def compress_session(
    state: StateStore,
    session_id: str,
    *,
    profile: Optional[ProviderProfile] = None,
    model: Optional[str] = None,
    threshold: int = 60_000,
    target: int = 20,
    use_model_summary: bool = False,
) -> CompressionResult:
    """Compress a session's message history.

    Strategy: keep the system + first user message + last ``target``
    messages verbatim. Replace everything in between with a single
    user-role message containing the summary.

    The summary becomes a new message inserted at the right position;
    the older messages are deleted. We do NOT modify the SQLite
    cascade rules — the old rows are removed via ``DELETE``.
    """
    msgs = state.list_messages(session_id)
    if not msgs:
        return CompressionResult(
            triggered=False, tokens_before=0, tokens_after=0,
            messages_before=0, messages_after=0, method="skipped",
        )

    tokens_before = estimate_messages_tokens(msgs)
    if threshold > 0 and tokens_before < threshold:
        return CompressionResult(
            triggered=False, tokens_before=tokens_before, tokens_after=tokens_before,
            messages_before=len(msgs), messages_after=len(msgs), method="skipped",
        )

    # Identify the slice to compress.
    head_count = 1  # the very first user message
    if len(msgs) <= head_count + target:
        return CompressionResult(
            triggered=False, tokens_before=tokens_before, tokens_after=tokens_before,
            messages_before=len(msgs), messages_after=len(msgs), method="skipped",
        )
    head = msgs[:head_count]
    middle = msgs[head_count:len(msgs) - target]
    tail = msgs[len(msgs) - target:]

    # --- Optimisation pass 1: deduplicate identical tool results ---
    _deduplicate_tool_results(middle)

    # --- Optimisation pass 2: replace old tool results with summaries ---
    for m in middle:
        if m.role == "tool" and m.content and len(m.content) > 200:
            # Find the matching assistant tool_call to get name + args.
            tool_name = ""
            tool_args: dict = {}
            for asst in middle:
                if asst.role == "assistant" and asst.tool_calls:
                    for tc in asst.tool_calls:
                        if tc.get("id") == m.tool_call_id:
                            tool_name = (tc.get("function") or {}).get("name", "")
                            raw = (tc.get("function") or {}).get("arguments", "")
                            if raw:
                                try:
                                    tool_args = json.loads(raw)
                                except (json.JSONDecodeError, ValueError):
                                    pass
            if tool_name:
                m.content = _summarize_tool_result(tool_name, tool_args, m.content)
            else:
                # Fallback: just truncate.
                m.content = m.content[:500] + "\n... [truncated for compression]"

    # --- Optimisation pass 3: truncate large tool_call args in assistant msgs ---
    for m in middle:
        if m.role == "assistant" and m.tool_calls:
            m.tool_calls = _truncate_tool_call_args(m.tool_calls)

    # Build the summary.
    summary_text = ""
    method = "count"
    if use_model_summary and profile is not None and model:
        summary_text = _summarise_via_model(_format_messages_for_summary(middle), profile, model)
        if summary_text:
            method = "summary"
    if not summary_text:
        summary_text = _count_based_summary(middle)
        method = "count"

    summary_msg = Message(
        role="user",
        content=(
            "[Conversation summary — older messages compressed to save context]\n\n"
            + summary_text
        ),
    )

    # Persist: delete middle, insert summary after head, keep tail.
    with state.transaction() as c:
        for m in middle:
            c.execute("DELETE FROM messages WHERE id = ?", (m.id,))
        # Insert summary at the end of the head. Use the timestamp of
        # the first message of the middle slice so the order is stable.
        insert_at = middle[0].created_at
        c.execute(
            "INSERT INTO messages(session_id, role, content, tool_calls, tool_call_id, name, created_at)"
            " VALUES(?,?,?,?,?,?,?)",
            (session_id, summary_msg.role, summary_msg.content, None, None, None, insert_at - 0.001),
        )

    # Recompute for the result.
    new_msgs = state.list_messages(session_id)
    tokens_after = estimate_messages_tokens(new_msgs)

    return CompressionResult(
        triggered=True,
        tokens_before=tokens_before,
        tokens_after=tokens_after,
        messages_before=len(msgs),
        messages_after=len(new_msgs),
        method=method,
        summary=summary_text,
    )


