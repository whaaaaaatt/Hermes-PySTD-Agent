_PERSIST_DIR = "hermes-results"
_DEFAULT_PERSIST_THRESHOLD = 100_000  # chars
_DEFAULT_PREVIEW_SIZE = 1_500


def maybe_persist_tool_result(
    content: str,
    tool_name: str = "",
    tool_use_id: str = "",
    threshold: int = _DEFAULT_PERSIST_THRESHOLD,
    preview_size: int = _DEFAULT_PREVIEW_SIZE,
) -> str:
    """If ``content`` exceeds the threshold, write it to a temp file and
    return a preview + path reference. Otherwise return as-is.

    This prevents a single massive tool result (e.g. reading a 5000-line
    file) from blowing the context window.
    """
    if len(content) <= threshold:
        return content

    import os
    import tempfile
    from pathlib import Path

    # Determine persist directory.
    try:
        from ..paths import get_hermes_home
        persist_dir = get_hermes_home() / _PERSIST_DIR
    except Exception:  # noqa: BLE001
        persist_dir = Path(tempfile.gettempdir()) / _PERSIST_DIR
    persist_dir.mkdir(parents=True, exist_ok=True)

    # Write full output to disk.
    fname = f"{tool_use_id or 'result'}.txt"
    fpath = persist_dir / fname
    try:
        fpath.write_text(content, encoding="utf-8", errors="replace")
    except OSError:
        # If we can't write, just truncate inline.
        return content[:preview_size] + f"\n... [truncated, {len(content)} chars total]"

    # Generate preview (break at last newline within preview_size).
    preview = content[:preview_size]
    last_nl = preview.rfind("\n")
    if last_nl > preview_size // 2:
        preview = preview[:last_nl + 1]

    size_kb = len(content) / 1024
    return (
        f"<persisted-output>\n"
        f"This tool result was too large ({len(content)} chars, {size_kb:.1f} KB).\n"
        f"Full output saved to: {fpath}\n"
        f"Use read_file with offset and limit to access specific sections.\n"
        f"Preview (first {len(preview)} chars):\n"
        f"{preview}"
        f"</persisted-output>"
    )
